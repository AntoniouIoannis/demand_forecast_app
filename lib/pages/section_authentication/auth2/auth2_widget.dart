  Future<void> _ensureUserProfileDocument(BaseAuthUser user) async {
    final uid = user.uid;
    if (uid == null || uid.isEmpty) {
      return;
    }

    final userDocRef = UsersRecord.collection.doc(uid);
    final userDoc = await userDocRef.get();
    if (userDoc.exists) {
      return;
    }

    await userDocRef.set(
      createUsersRecordData(
        email: user.email ?? '',
        uid: uid,
        displayName: user.displayName ?? '',
        photoUrl: user.photoUrl ?? '',
        phoneNumber: user.phoneNumber ?? '',
        createdTime: getCurrentTimestamp,
      ),
    );

    await userDocRef.collection('upload-files').doc('_init').set({
      'createdAt': getCurrentTimestamp,
    });
    await userDocRef.collection('processing-files').doc('_init').set({
      'createdAt': getCurrentTimestamp,
    });
    await userDocRef.collection('received-files').doc('_init').set({
      'createdAt': getCurrentTimestamp,
    });
  }

  Future<void> _handleProviderAuth(
    Future<BaseAuthUser?> Function() signIn,
  ) async {
    GoRouter.of(context).prepareAuthEvent();
    final user = await signIn();
    if (user == null) {
      return;
    }

    await _ensureUserProfileDocument(user);

    if (!mounted) {
      return;
    }
    context.goNamedAuth(ImportDataWidget.routeName, context.mounted);
  }

  Future<void> _showUnverifiedEmailDialog(String email) async {
    if (!mounted) {
      return;
    }

    final action = await showDialog<String>(
      context: context,
      builder: (dialogContext) {
        return AlertDialog(
          title: Text('Email not verified'),
          content: Text(
            'You are logged in, but your email is not verified yet. '
            'Please click the verification link we sent to ${email.isEmpty ? 'your email address' : email}.',
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(dialogContext, 'cancel'),
              child: Text('Cancel'),
            ),
            TextButton(
              onPressed: () => Navigator.pop(dialogContext, 'verify'),
              child: Text('Open Verify Page'),
            ),
            TextButton(
              onPressed: () => Navigator.pop(dialogContext, 'resend'),
              child: Text('Resend Email'),
            ),
          ],
        );
      },
    );

    if (!mounted) {
      return;
    }

    if (action == 'resend') {
      await authManager.sendEmailVerification();
      if (!mounted) {
        return;
      }
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Verification email sent again.'),
        ),
      );
    }

    if (action == 'verify' || action == 'resend') {
      context.pushNamedAuth(VerifyEmailWidget.routeName, context.mounted);
    }
  }

